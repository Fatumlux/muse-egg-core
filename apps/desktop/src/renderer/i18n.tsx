const I18nContext = createContext<I18nContextValue | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>(() => {
    const stored = localStorage.getItem(storageKey);
    return stored === "en" || stored === "zh-TW" ? stored : "zh-TW";
  });

  const value = useMemo<I18nContextValue>(() => {
    const dictionary = dictionaries[language];
    return {
      language,
      setLanguage(nextLanguage) {
        setLanguageState(nextLanguage);
      },
      t(key, values) {
        return format(dictionary[key] ?? dictionaries["zh-TW"][key] ?? key, values);
      }
    };
  }, [language]);

  useEffect(() => {
    localStorage.setItem(storageKey, language);
    document.documentElement.lang = language === "zh-TW" ? "zh-Hant" : "en";
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const value = useContext(I18nContext);
  if (!value) {
    throw new Error("useI18n must be used within I18nProvider.");
  }
  return value;
}

export function translateStatic(key: string, values?: Record<string, string | number | undefined>): string {
  const stored = typeof localStorage === "undefined" ? undefined : localStorage.getItem(storageKey);
  const language: AppLanguage = stored === "en" || stored === "zh-TW" ? stored : "zh-TW";
  return format(dictionaries[language][key] ?? dictionaries["zh-TW"][key] ?? key, values);
}

export function LanguageSwitcher() {
  const { language, setLanguage, t } = useI18n();

  return (
    <label className="language-switcher">
      <span>
        <Languages size={14} />
        {t("language.label")}
      </span>
      <select value={language} onChange={(event) => setLanguage(event.target.value as AppLanguage)}>
        <option value="zh-TW">{t("language.zhTW")}</option>
        <option value="en">{t("language.en")}</option>
      </select>
    </label>
  );
}

function format(template: string, values?: Record<string, string | number | undefined>): string {
  if (!values) {
    return template;
  }
  return Object.entries(values).reduce(
    (result, [key, value]) => result.replaceAll(`{${key}}`, value === undefined ? "" : String(value)),
    template
  );
}
